# replace ("please enter your age") with your age below
age = ( "Please enter your age"  )

print(age)

# driving age is 16+
if age >= 16:
    print("You are old enough to drive.")
else:
    print("You are not old enough to drive.")

# voting age is 18+
if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote yet.")

# legal drinking age is 21+
if age >= 21:
    print("You can legally buy alcohol.")
else:
    print("You cannot legally buy alcohol.")

# senior discount age is 65+
if age >= 65:
    print("You are eligible for a senior discount.")
else:
    print("You are not eligible for a senior discount.")
